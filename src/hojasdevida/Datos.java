package hojasdevida;

/**
 *
 * @author Jonathan
 */
public class Datos {
    
    public String imagen;
    
    private String cedula;
    
    private String correo;
    
    private String nombres;
    
    private String profesion;
    
    private char genero;
    
    private int fecNacDia;
    
    private int fecNacMes;
    
    private int fecNacAnio;

    public Datos() {
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getProfesion() {
        return profesion;
    }

    public void setProfesion(String profesion) {
        this.profesion = profesion;
    }

    public char getGenero() {
        return genero;
    }

    public void setGenero(char genero) {
        this.genero = genero;
    }

    public int getFecNacDia() {
        return fecNacDia;
    }

    public void setFecNacDia(int fecNacDia) {
        this.fecNacDia = fecNacDia;
    }

    public int getFecNacMes() {
        return fecNacMes;
    }

    public void setFecNacMes(int fecNacMes) {
        this.fecNacMes = fecNacMes;
    }

    public int getFecNacAnio() {
        return fecNacAnio;
    }

    public void setFecNacAnio(int fecNacAnio) {
        this.fecNacAnio = fecNacAnio;
    }
    
    
    
}
